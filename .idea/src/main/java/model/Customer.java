package model;

public class Customer {
    int id;
    String name;
    long phone;
    String city;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public long getPhone() {
        return phone;
    }

    public String getCity() {
        return city;
    }
}
